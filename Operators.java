public class Operators {
    public static void main(String[] args) {
        int num1 = 90;
        int num2 = 100;
        
        // is equal to
        System.out.println(num1 == num2);
        
        // is not equal to
        System.out.println(num1 != num2);

        // is greater than
        System.out.println(num1 > num2);

        // is less than
        System.out.println(num1 < num2);

        // is greater than or equal to
        System.out.println(num1 >= num2);

        // is less than equal to
        System.out.println(num1 <= num2);
    }
    
}
